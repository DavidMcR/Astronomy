package particles;

public class Mote {
	private double x;
	private double y;
	private double z;
	private double xVelocity;
	private double yVelocity;
	private double zVelocity;
	private double mass;
	public Mote(){
		
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public double getZ() {
		return z;
	}
	public void setZ(double z) {
		this.z = z;
	}
	public double getMass() {
		return mass;
	}
	public void setMass(double mass) {
		this.mass = mass;
	}
	
	

}
